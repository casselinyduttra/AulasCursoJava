/*
 * um valor qualquer seja atribuido a uma variavel
 */
public class OperadoresAtribuicao {

	public static void main(String[] args) {
		
		double numero = 7.0;
		
		numero = numero + 4;
		System.out.println("numero + 4:  "+numero);
		
		numero += 4;
		System.out.println("numero += 4:  "+numero);
		
		numero -= 4;
		System.out.println("numero -= 4:  "+numero);
		
		numero *= 4;
		System.out.println("numero *= 4:  "+numero);
		
		numero /= 4;
		System.out.println("numero /= 4:  "+numero);
		
		numero %= 4;
		System.out.println("numero += 4:  "+numero);
	}

}
